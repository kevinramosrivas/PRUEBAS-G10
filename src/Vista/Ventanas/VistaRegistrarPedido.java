    @Override
    public boolean faltanLlenarDatos() {
        
        boolean resultado = false; 
        if(faltanLlenarRB())
        {
            resultado = true;
        }
        return resultado;
    }

    @Override
    public boolean faltanLlenarRB() {
        boolean resultado = false;
        
        if(rbtnAmericanExpress.isSelected()==false&&
                rbtnMasterCard.isSelected()==false&&
                rbtnPlantilla.isSelected()==false &&
                rbtnTarjeta.isSelected()==false &&
                rbtnVisa.isSelected()==false)
        {
            resultado = true;
        }
        
        
        if(rbtnAmericanExpress.isSelected() || rbtnMasterCard.isSelected() || rbtnVisa.isSelected())
        {
            if(rbtnPlantilla.isSelected()==false && rbtnTarjeta.isSelected()==false)
            {
                resultado = true;
            }
           
        }
        
        if(rbtnTarjeta.isSelected() || rbtnPlantilla.isSelected())
        {
            if(rbtnAmericanExpress.isSelected()==false && rbtnMasterCard.isSelected()==false && rbtnVisa.isSelected()==false)
            {
                resultado = true;
            }
        }
        
        
        return resultado;
    }
